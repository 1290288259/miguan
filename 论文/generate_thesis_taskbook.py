TASK_DOCX = None
for f in os.listdir('.'):
    if f.endswith('.docx') and [ord(c) for c in f[:3]] == [0x4efb, 0x52a1, 0x4e66]:
        TASK_DOCX = f
        break

refs = []
if TASK_DOCX:
    try:
        from docx import Document as DocxReader
        tdoc = DocxReader(TASK_DOCX)
        raw = "\n".join(p.text for p in tdoc.paragraphs if p.text.strip())
        import re
        refs = re.findall(r"\[\d+\].*", raw)
    except Exception:
        refs = []


